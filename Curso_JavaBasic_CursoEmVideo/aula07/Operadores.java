package aula07;

 public class Operadores {
    public static void main(String[] args) {

        int n1 = 3;
        int n2 = 5;
        float m = (n1 + n2) / 2f;
        System.out.println("A média é igual a " + m);

        m++;
        System.out.println("Mas por bom comportamento " + m);

        int numero = 10;
        numero += 5;
        System.out.println("O número agora é " + numero);

    }
    
}