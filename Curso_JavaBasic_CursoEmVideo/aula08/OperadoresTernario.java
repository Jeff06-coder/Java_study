package aula08;

public class OperadoresTernario {

    public static void main(String[] args) {
        
        int n1, n2, r;
        
        n1 = 5;
        n2 = 8;
        
        // Estrutura condicional usando o operador ternário
        r = (n1 > n2) ? n1 : n2;
        
        System.out.println("O maior valor entre " + n1 + " e " + n2 + " é " + r);
        

    }
    
}
